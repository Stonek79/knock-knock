#!/usr/bin/env bash
# scripts/clear-db-data.sh

# Обертка для запуска JS-скрипта очистки БД.
# Использует текущее окружение из .env

set -e

# Цвета для вывода
RED='\033[0;31m'
NC='\033[0m'

echo -e "${RED}!!! ПРЕДУПРЕЖДЕНИЕ: Очистка базы данных !!!${NC}"
echo "Используется окружение из app/.env"
echo "Это действие удалит ВСЕ данные (сообщения, комнаты, пользователи)."
read -p "Вы уверены, что хотите продолжить? (y/n) " -n 1 -r
echo
if [[ ! $REPLY =~ ^[Yy]$ ]]
then
    echo "Отмена."
    exit 1
fi

node infra/scripts/clear_db.cjs "$@"
