/**
 * Утилита для получения числовых значений размеров иконок из CSS-переменных.
 *
 * Проблема: Lucide React принимает `size` как SVG-атрибут `width`/`height`,
 * а браузер не понимает CSS-переменные в SVG-атрибутах (только в CSS-свойствах).
 *
 * Решение: используем числовые константы, соответствующие значениям токенов.
 * При изменении `--scale-factor` иконки не масштабируются — это известное ограничение,
 * которое решается через CSS-класс `.icon` с `width`/`height` через CSS-переменные.
 *
 * @example
 * // Базовое использование
 * import { ICON_SIZE } from "@/lib/utils/iconSize";
 * <Search size={ICON_SIZE.sm} />
 *
 * // Для масштабируемых иконок — используй CSS-класс:
 * <Search className={styles.icon} />
 * // .icon { width: var(--size-icon-sm); height: var(--size-icon-sm); }
 */

/**
 * Числовые значения размеров иконок (в пикселях).
 * Соответствуют CSS-токенам при scale-factor = 1.
 */
export const ICON_SIZE = {
    /** 14px — мелкие элементы, чипы, бейджи */
    xs: 14,
    /** 18px — навигация, кнопки, поля ввода */
    sm: 18,
    /** 24px — основные иконки действий */
    md: 24,
    /** 32px — пустые состояния, заглушки */
    lg: 32,
    /** 48px — крупные иллюстративные иконки */
    xl: 48,
    /** 64px — hero-иконки */
    "2xl": 64,
    xxl: 64,
} as const;

/** Тип ключей размеров иконок */
export type IconSizeKey = keyof typeof ICON_SIZE;
